package questionProgram;

public class Reverse_number  
{
	public void revnum()	// method to print  numbers in reverse from 10 to 1 . 								
	{
		int i;												
		System.out.println("\n Displaying 1 to 10 in reverse order : ");	
		for(i=10;i>=1;i--)									//for loop
		{
			System.out.print(i+" ,");
		}
	}
}